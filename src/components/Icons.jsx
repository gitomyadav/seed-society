// Subject icon mapping — returns a letter-based icon with subject color
export function SubjectIcon({ subject, size = 20 }) {
  const subjectMap = {
    Physics: { letter: 'Ph', color: '#3B82F6', bg: '#EFF6FF' },
    Chemistry: { letter: 'Ch', color: '#8B5CF6', bg: '#F5F3FF' },
    Mathematics: { letter: 'Ma', color: '#D97706', bg: '#FFFBEB' },
    Biology: { letter: 'Bi', color: '#059669', bg: '#ECFDF5' },
    English: { letter: 'En', color: '#DC2626', bg: '#FEF2F2' },
    'Computer Science': { letter: 'CS', color: '#0891B2', bg: '#ECFEFF' },
    Nepali: { letter: 'Ne', color: '#E11D48', bg: '#FFF1F2' },
    Accountancy: { letter: 'Ac', color: '#65A30D', bg: '#F7FEE7' },
  };

  const info = subjectMap[subject] || { letter: subject?.[0] || '?', color: '#6B7280', bg: '#F3F4F6' };

  return (
    <div style={{
      width: size + 18,
      height: size + 18,
      borderRadius: 8,
      background: info.bg,
      display: 'flex',
      alignItems: 'center',
      justifyContent: 'center',
      fontFamily: 'var(--font-heading)',
      fontWeight: 700,
      fontSize: size * 0.55,
      color: info.color,
      flexShrink: 0,
      letterSpacing: '-0.02em',
    }}>
      {info.letter}
    </div>
  );
}

